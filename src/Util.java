public class Util {

    public static void printArray(String[] args) {
        for (String temp : args) {
            System.out.print(temp + " ");
        }
        System.out.println();
    }

    public static void printArray(int[] args) {
        for (int temp : args) {
            System.out.print(temp + " ");
        }
        System.out.println();
    }
}
